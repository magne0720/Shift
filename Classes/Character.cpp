//時間を初期化
void Character::resetTimer() 
{
	delayTimer = 0;
	keepTimer = 0;
	standbyTimer = 0;
	actionClock = 0;
};

void Character::pushAction(ACTION action) 
{
	isCommand = true;
	myAction = action;
}
